package Soal6;


public class HasilRataRata {
    private double rataRata;

    public HasilRataRata(double rataRata) {
        this.rataRata = rataRata;
    }

    public double getRataRata() {
        return rataRata;
    }

    public void tampilkan() {
        System.out.println("Rata-rata nilai = " + rataRata);
    }
}

